export async function generateMetadata({ params }: { params: { slug: string } }): Promise<Metadata> {
  const post = posts[params.slug]
  if (!post) return { title: 'Post Not Found | Aethla Care' }
  return {
    title: `${post.title} | Aethla Care Qatar`,
    description: post.content.slice(0, 160),
  }
}

export default async function BlogPostPage({ params }: { params: { slug: string } }) {
  // First check static posts
  let post = posts[params.slug]

  // If not found in static, try API
  if (!post) {
    try {
      const res = await fetch(
        `${process.env.NEXT_PUBLIC_API_URL}/public/blog/${params.slug}`,
        { next: { revalidate: 60 } }
      )
      if (res.ok) {
        const data = await res.json()
        post = {
          title: data.title,
          category: data.category,
          author: data.author || 'Aethla Care Team',
          date: data.publishedAt ? new Date(data.publishedAt).toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' }) : '',
          readTime: data.readTime || 5,
          img: data.featuredImage || 'https://images.unsplash.com/photo-1576091160399-112ba8d25d1d?w=1200&q=80&auto=format&fit=crop',
          content: data.content,
        }
      }
    } catch {}
  }

  if (!post) {
    return (
      <div className="section-pad text-center">
        <h1 className="text-display-sm mb-4">Post Not Found</h1>
        <p className="text-body-lg text-neutral-500 mb-6">This article could not be found.</p>
        <Link href="/blog" className="btn-primary btn-lg">← Back to Blog</Link>
      </div>
    )
  }

  // Parse content — **bold** and newlines
  const parseContent = (text: string) =>
    text.split('\n\n').map((para, i) => {
      if (para.startsWith('**') && para.includes('**\n')) {
        const [heading, ...rest] = para.split('\n')
        return (
          <div key={i} className="mb-5">
            <h3 className="text-heading-md font-poppins text-neutral-800 mb-2">
              {heading.replace(/\*\*/g, '')}
            </h3>
            <p className="text-body-md text-neutral-600 leading-relaxed">{rest.join('\n')}</p>
          </div>
        )
      }
      return <p key={i} className="text-body-md text-neutral-600 leading-relaxed mb-5">{para}</p>
    })

  return (
    <>
      {/* Hero */}
      <section className="relative min-h-[400px] flex items-end pb-12">
        <div className="absolute inset-0" style={{
          backgroundImage: `url('${post.img}')`,
          backgroundSize: 'cover', backgroundPosition: 'center',
        }} />
        <div className="absolute inset-0" style={{ background: 'linear-gradient(to top, rgba(13,43,62,0.95) 0%, rgba(13,43,62,0.5) 60%, transparent 100%)' }} />
        <div className="container-max px-4 md:px-8 relative z-10">
          <span className="badge-primary text-xs mb-3 inline-block">{post.category}</span>
          <h1 className="text-display-md text-white mb-4 max-w-3xl">{post.title}</h1>
          <div className="flex items-center gap-4 text-white/60 text-caption">
            <span>{post.author}</span>
            <span>&middot;</span>
            <span>{post.date}</span>
            <span>&middot;</span>
            <span>{post.readTime} min read</span>
          </div>
        </div>
      </section>

      {/* Content */}
      <section className="section-pad bg-white">
        <div className="container-max">
          <div className="grid lg:grid-cols-3 gap-12">
            <article className="lg:col-span-2">
              {parseContent(post.content)}
            </article>

            {/* Sidebar */}
            <aside className="space-y-5">
              <div className="card p-6">
                <h3 className="text-heading-sm font-poppins mb-4">Book a Free Consultation</h3>
                <p className="text-body-sm text-neutral-500 mb-4">Speak with our care team about your family&apos;s specific needs.</p>
                <Link href="/contact" className="btn-primary w-full text-center block py-3">Get in Touch</Link>
              </div>

              <div className="card p-6">
                <h3 className="text-heading-sm font-poppins mb-4">Our Services</h3>
                <div className="flex flex-col gap-2">
                  {['Elderly Care', 'Disability Support', 'Newborn Care', 'Maternity Care', 'Home Wellness', 'Telehealth'].map(s => (
                    <Link key={s} href={`/services/${s.toLowerCase().replace(' ', '-')}`}
                      className="text-body-sm text-primary-500 hover:underline">{s}</Link>
                  ))}
                </div>
              </div>
            </aside>
          </div>

          <div className="mt-10 pt-8 border-t border-neutral-100">
            <Link href="/blog" className="btn-outline btn-sm">← Back to Blog</Link>
          </div>
        </div>
      </section>
    </>
  )
}